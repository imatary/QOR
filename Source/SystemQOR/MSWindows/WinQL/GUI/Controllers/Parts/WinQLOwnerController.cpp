	//--------------------------------------------------------------------------------
	bool COwnerController::ProcessMessage( COSWindow& Window, Cmp_long_ptr& lResult, unsigned int uMsg, Cmp_uint_ptr wParam, Cmp_long_ptr lParam )
	{
		_WINQ_FCONTEXT( "COwnerController::ProcessMessage" );

		bool bProcessed = ProcessHook( Window, lResult, uMsg, wParam, lParam );

		switch ( uMsg )
		{
		case COSWindow::wmEnterMenuLoop:
			{
				bool bTrack = wParam ? true : false;
				OnEnterMenuLoop( Window, bTrack );
				lResult = 0;
				bProcessed = true;
			}
			break;
		case COSWindow::wmExitMenuLoop:
			{
				bool bShortcut = wParam ? true : false;
				OnExitMenuLoop( Window, bShortcut );
				bProcessed = true;
			}
			break;
		case COSWindow::wmEnterIdle:
			{
				OnEnterIdle( Window, wParam, COSWindow::FromHandle( CWindowHandle( 0, (void*)(lParam) ).Ref() ) );
				lResult = 0;
				bProcessed = true;
			}
			break;
		}

		return bProcessed;
	}

