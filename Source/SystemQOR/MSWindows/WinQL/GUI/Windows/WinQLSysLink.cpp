	long CSysLink::OnNotify( NotificationMessageHeader* pHdr )
	{
		_WINQ_FCONTEXT( "CSysLink::OnNotify" );
		long lResult = 0;

		if( pHdr != 0 )
		{
			switch ( pHdr->m_uiCode )
			{
			default:
				{
					lResult = OnUnknownNotification( pHdr );
				}
				break;
			}
		}
		return lResult;
	}

