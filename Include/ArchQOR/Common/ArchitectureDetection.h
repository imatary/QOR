#	define QOR_ARCH	QOR_ARCH_X86_64
#	define QOR_ARCH_ROOT_FOLDER	x86
#elif defined(__SYSC_ZARCH__)
#	define QOR_ARCH QOR_ARCH_Z
#	define QOR_ARCH_ROOT_FOLDER	Zarch
#elif defined(__s390__) || defined(__s390x__)
