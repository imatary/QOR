	//--------------------------------------------------------------------------------
	bool CSession::BlockShutdown(COSWindow::refType Wnd, const wchar_t* pwszReason) const
	{
		_WINQ_FCONTEXT("CSession::BlockShutdown");
		bool bResult = false;
		bResult = ShutdownBlockReasonCreate(Wnd, pwszReason);
		return bResult;
	}

