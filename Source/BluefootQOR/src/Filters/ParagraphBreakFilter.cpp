	//------------------------------------------------------------------------------
	CParagraphBreakFilter::CParagraphBreakFilter() : CBFFilter()
	,	m_bHighBreak( false )
	,	m_Break( eCRLF )
	{
	}

	//------------------------------------------------------------------------------
	CParagraphBreakFilter::~CParagraphBreakFilter()
	{
	}

	//------------------------------------------------------------------------------
	CParagraphBreakFilter::CParagraphBreakFilter( const CParagraphBreakFilter& src ) : CBFFilter( src )
	{
	}

