	//--------------------------------------------------------------------------------
	bool CIconizedController::ProcessMessage( COSWindow& Window, Cmp_long_ptr& lResult, unsigned int uMsg, Cmp_uint_ptr wParam, Cmp_long_ptr lParam )
	{
		_WINQ_FCONTEXT( "CIconizedController::ProcessMessage" );

		bool bProcessed = ProcessHook( Window, lResult, uMsg, wParam, lParam );

		switch ( uMsg )
		{
		case COSWindow::wmQueryOpen:
			{
				lResult = ( OnQueryOpen( Window ) ? 1 : 0 ); 
				bProcessed = true;
			}
			break;

		case COSWindow::wmPaintIcon:
			{
				bProcessed = true;
			}
			break;

		case COSWindow::wmIconEraseBkgnd:
			{
				bProcessed = true;
			}
			break;

		case COSWindow::wmQueryDragIcon:
			{
				CIcon::refType refIcon = OnQueryDragIcon( Window );
				lResult = 0;
				if( !refIcon.IsNull() )
				{
					lResult = reinterpret_cast< Cmp_long_ptr >( refIcon->Handle()->AsHandle().ptr() );
				}
				bProcessed = true;
			}
			break;
		}

		return bProcessed;
	}

	//--------------------------------------------------------------------------------
	bool CIconizedController::OnQueryOpen( COSWindow& Window )
	{
		_WINQ_FCONTEXT( "CIconizedController::OnQueryOpen" );
		return true;
	}

	//--------------------------------------------------------------------------------
	CIcon::refType CIconizedController::OnQueryDragIcon( COSWindow& Window )
	{
		_WINQ_FCONTEXT( "CIconizedController::OnQueryDragIcon" );
		CIcon::refType refIcon;
		return refIcon;
	}

