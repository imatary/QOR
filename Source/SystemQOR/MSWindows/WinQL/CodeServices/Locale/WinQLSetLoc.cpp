	//--------------------------------------------------------------------------------
	int CSetLoc::__ascii_strnicmp ( const char* first, const char* last, size_t count )
	{
		if( count )
		{
			int f = 0;
			int l = 0;

			do
			{
				if ( ( ( f = (unsigned char)( *( first++ ) ) ) >= 'A' ) && ( f <= 'Z' ) )
				{
					f -= 'A' - 'a';
				}

				if ( ( ( l = (unsigned char)( *( last++ ) ) ) >= 'A' ) && ( l <= 'Z' ) )
				{
					l -= 'A' - 'a';
				}
			}
			while ( --count && f && ( f == l ) );

			return ( f - l );
		}
		else
		{
			return 0;
		}
	}

