#include "CompilerQOR.h"
#include "CodeQOR/Tracing/FunctionContextBase.h"
#include "CodeQOR/Parameters/ParamChecking.h"
#include "CodeQOR/Parameters/PChecks/PointerChecks.h"
#include "CodeQOR/ErrorSystem/ParamErrorDomain.h"

//--------------------------------------------------------------------------------
namespace nsParamChecking
{
	//--------------------------------------------------------------------------------
	//Pointer parameter check implementations
	
	//--------------------------------------------------------------------------------	
	//Ensure that pointer is not NULL
	void CNullPointerCheck::Check( const void* p )
	{
		if( p == 0 )
		{
			nsCodeQOR::CError::CBaseErrorParams Params( nsCodeQOR::CBaseErrorDomain::RANGE_ERROR, "Null pointer error" );
			nsCodeQOR::CError::Raise( __FILE__, __LINE__, __QCMP_FUNCSIG__( "CNullPointerCheck::Check" ), nsCodeQOR::CObjectContextBase::nullContext,
				Params, &(nsCodeQOR::ParamErrorDomain()), nsCodeQOR::CError::ERR_LVL_CONTINUE );				
		}
	}

	//--------------------------------------------------------------------------------	
	//Ensure pointer + size points to write accessible memory
	void CWriteablePointerCheck::Check( const void* p, int iSize )
	{
		CNullPointerCheck::Check( p );
		if( p )
		{
			unsigned char* pc = reinterpret_cast< unsigned char* >( const_cast< void *>( p ) );
			unsigned char c1 = *pc;
			unsigned char c2 = *( pc + iSize );
			*pc = 0;
			*( pc + iSize ) = 0;
			*pc = c1;
			*( pc + iSize ) = c2;
		}
	}

	//--------------------------------------------------------------------------------	
	//Ensure pointer + size points to read accessible memory
	void CReadablePointerCheck::Check( const void* p, int iReadableSize )
	{
		CNullPointerCheck::Check( p );
		if( p )
		{
			unsigned char* pc = reinterpret_cast< unsigned char* >( const_cast< void *>( p ) );
			unsigned char c1 = *pc;
			unsigned char c2 = *( pc + iReadableSize );
			c1 = 0;
			c2 = 0;
		}
	}

	//--------------------------------------------------------------------------------	
	//Ensure pointer is null or pointer + size points to read accessible memory
	void CNullOrReadablePointerCheck::Check( const void* p, int iReadableSize )
	{
		if( p != 0 )
		{
			CReadablePointerCheck::Check( p, iReadableSize );
		}
	}

	//--------------------------------------------------------------------------------	
	void CRWPointerCheck::Check( const void* p, int iRWSize )
	{
		nsParamChecking::CWriteablePointerCheck::Check( p, iRWSize );
	}

	//--------------------------------------------------------------------------------	
	//Ensure pointer points to executable memory
	void CCodePointerCheck::Check( void* p )
	{
		CNullPointerCheck::Check( p );
	}

}//nsParamChecking

