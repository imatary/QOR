	bool IsSpecialDataObject( _t& d )
	{
		return (((Cmp_long_ptr)( d ) >= SPECIAL_DOBJ_MIN ) && ( (Cmp_long_ptr)(d) <= SPECIAL_DOBJ_MAX ) );
	}

