	size_t Cwchar::wcslen( const wchar_t* s )
	{
		_WINQ_FCONTEXT( "Cwchar::wcslen" );
		size_t stResult = 0;
		if( s != 0 )
		{
			const wchar_t* pwchEOS = s;

			while( *pwchEOS++ ) ;

			stResult = static_cast< size_t >( pwchEOS - s - 1 );
		}
		return stResult;
	}

