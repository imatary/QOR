#include "WinQL/CodeServices/WinQLPolicy.h"
#include "WinQL/Application/Threading/WinQLCriticalSection.h"
#include "WinQL/COM/RunTime/Sessions/WinQLCOSecuritySession.h"
#include "WinQL/Application/ErrorSystem/WinQLError.h"
#include "WinQAPI/OLE32.h"

//--------------------------------------------------------------------------------
namespace nsWin32
{
	__QOR_IMPLEMENT_OCLASS_LUID( CCOSecuritySession );

	//--------------------------------------------------------------------------------
	CCOSecuritySession::CCOSecuritySession( CSecurityDescriptor* pSecDesc, long cAuthSvc, SoleAuthenticationService* pasAuthSvc, unsigned long ulAuthnLevel, unsigned long ulImpLevel, void* pAuthList, unsigned long ulCapabilities )
	{
		_WINQ_FCONTEXT( "CCOSecuritySession::CCOSecuritySession" );
		m_lStatus = m_Library.CoInitializeSecurity( reinterpret_cast< ::PSECURITY_DESCRIPTOR >( pSecDesc ), cAuthSvc, reinterpret_cast< SOLE_AUTHENTICATION_SERVICE* >( pasAuthSvc ), (void*)0, ulAuthnLevel, ulImpLevel, pAuthList, ulCapabilities, 0 );
	}

	//--------------------------------------------------------------------------------
	CCOSecuritySession::~CCOSecuritySession()
	{
		_WINQ_FCONTEXT( "CCOSecuritySession::~CCOSecuritySession" );
	}

	//--------------------------------------------------------------------------------
	long CCOSecuritySession::QueryAuthenticationServices( unsigned long* pcAuthSvc, SoleAuthenticationService** asAuthSvc )
	{
		_WINQ_FCONTEXT( "CCOSecuritySession::QueryAuthenticationServices" );
		return m_Library.CoQueryAuthenticationServices( pcAuthSvc, reinterpret_cast< SOLE_AUTHENTICATION_SERVICE** >( asAuthSvc ) );
	}

}//nsWin32
