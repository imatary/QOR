	bool CNCRenderController::ProcessMessage( COSWindow& Window, Cmp_long_ptr& lResult, unsigned int uMsg, Cmp_uint_ptr wParam, Cmp_long_ptr lParam )
	{
		_WINQ_FCONTEXT( "CNCRenderController::ProcessMessage" );

		bool bProcessed = ProcessHook( Window, lResult, uMsg, wParam, lParam );

		switch ( uMsg )
		{
		case COSWindow::wmNCCalcSize:
			{
				OnNCCalcSize( Window, lResult, wParam, lParam );
				bProcessed = true;
			}
			break;

		case COSWindow::wmNCPaint:
			{
				OnNCPaint( Window, lResult, wParam, lParam );
				bProcessed = true;
			}
			break;

		case COSWindow::wmNCHitTest:
			{
				OnNCHitTest( Window, lResult, wParam, lParam );
				bProcessed = true;
			}
			break;

		case COSWindow::wmSize:
			{
				OnNCSize( Window, lResult, wParam, lParam );					
			}
			break;

		//case WM_GETTITLEBARINFOEX:
			//{
				//TITLEBARINFOEX* pTitleBarInfo = reinterpret_cast< TITLEBARINFOEX* >( lParam );
				//OnGetTitleBarInfoEx( pWindow, pTitleBarInfo );
				//bProcessed = true;
			//}
			//break;
		}

		return bProcessed;
	}

	//--------------------------------------------------------------------------------
