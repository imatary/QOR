	void CServer::Setup( nsQOR::IApplication& Application )
	{		
		m_Application = Application.Ref();

		m_bPendingIO = OnListenResult( Listen() );

		m_Application.As< CWin32Application >()->AddWaitableObject( m_ConnectEvent.Ref() );
	}

