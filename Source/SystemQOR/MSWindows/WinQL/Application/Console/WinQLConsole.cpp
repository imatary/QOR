	//------------------------------------------------------------------------------
	int __stdcall CConsole::CtrlHandler(unsigned long CtrlType )
	{
		_WINQ_SFCONTEXT( "CConsole::CtrlHandler" );
		int iResult = 0;
		if( g_pConsole )
		{
			iResult = g_pConsole->InputStdHandle()->OnCtrl( CtrlType ) ? 1 : 0;
		}
		return iResult;
	}

