	int sched_get_priority_min(int pol)
	{
		if( pol < SCHED_MIN || pol > SCHED_MAX ) 
		{
			errno = EINVAL;
			return -1;
		}

		return nsWin32::CThread::Thread_Priority_Idle;
	}

	//----------------------------------------------------------------------
	int sched_get_priority_max(int pol)
	{
		if (pol < SCHED_MIN || pol > SCHED_MAX) 
		{
			errno = EINVAL;
			return -1;
		}

		return nsWin32::CThread::Thread_Priority_Time_Critical;
	}

