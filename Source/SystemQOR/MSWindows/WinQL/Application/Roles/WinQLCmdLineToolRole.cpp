//--------------------------------------------------------------------------------
namespace nsWin32
{	
	__QOR_IMPLEMENT_OCLASS_GUID( CCmdLineToolRole, 0xd0828572, 0xebd0, 0x47ad, 0xba, 0x8b, 0x8c, 0x99, 0x67, 0xec, 0xef, 0xe6 );

	nsCodeQOR::CTExternalRegEntry< CCmdLineToolRole > CCmdLineToolRole::RegEntry;

	//--------------------------------------------------------------------------------
	CCmdLineToolRole::CCmdLineToolRole() : nsQOR::CRoleImplBase()
	{
		_WINQ_FCONTEXT( "CCmdLineToolRole::CCmdLineToolRole" );
		AddSubSystem( *CThreading::ClassID(), m_Threading.Ref() );
		AddSubSystem( *CSystem::ClassID(), m_System.Ref() );
		AddSubSystem( *CStandardIO::ClassID(), m_StandardIO.Ref() );
		AddSubSystem( *CTerminal::ClassID(), m_Terminal.Ref() );
	}

	//--------------------------------------------------------------------------------
	CCmdLineToolRole::~CCmdLineToolRole()
	{
		_WINQ_FCONTEXT( "CCmdLineToolRole::~CCmdLineToolRole" );
	}

	//--------------------------------------------------------------------------------
	CCmdLineToolRole::CCmdLineToolRole( const CCmdLineToolRole& src ) : nsQOR::CRoleImplBase( src )
	{
		_WINQ_FCONTEXT( "CCmdLineToolRole::CCmdLineToolRole" );
	}

	//--------------------------------------------------------------------------------
	CCmdLineToolRole& CCmdLineToolRole::operator = ( const CCmdLineToolRole& src )
	{
		_WINQ_FCONTEXT( "CCmdLineToolRole::operator =" );
		if( &src != this )
		{
		}
		return *this;
	}

	//--------------------------------------------------------------------------------
	void CCmdLineToolRole::Setup( nsQOR::IApplication& Application )
	{		
		_WINQ_FCONTEXT( "CCmdLineToolRole::Setup" );
		m_Threading.Setup( Application );
		m_System.Setup( Application );
		m_Terminal.Setup( Application );
		m_StandardIO.Setup( Application );
	}

	//--------------------------------------------------------------------------------
	void CCmdLineToolRole::Shutdown( nsQOR::IApplication& Application )
	{
		_WINQ_FCONTEXT( "CCmdLineToolRole::Shutdown" );
		m_StandardIO.Shutdown( Application );
		m_Terminal.Shutdown( Application );
		m_System.Shutdown( Application );
		m_Threading.Shutdown( Application );
	}

}//nsWin32
