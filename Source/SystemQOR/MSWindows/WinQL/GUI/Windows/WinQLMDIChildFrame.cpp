	Cmp_long_ptr CMDIChildFrame::DefProc( unsigned int Msg, Cmp_uint_ptr wParam, Cmp_long_ptr lParam, CWindowHandle::refType hWnd )
	{
		_WINQ_FCONTEXT( "CMDIChildFrame::DefProc" );

		Cmp_long_ptr lResult = 0;
		
		COSWindow::refType refWindow = COSWindow::FromHandle( hWnd );
		if( refWindow != 0 )
		{
			lResult = refWindow->DefProc( Msg, wParam, lParam );
		}
		else
		{
			lResult = DefaultChildFrameProc( refWindow, Msg, wParam, lParam );				
		}
		return lResult;
	}

