#ifndef QOR_PREPROCESSOR_REMOVE_PARENS_H
#define QOR_PREPROCESSOR_REMOVE_PARENS_H

#include "CompilerQOR/Preprocessor/preprocessor/config/config.h"

#if QOR_PP_VARIADICS

#include "CompilerQOR/Preprocessor/preprocessor/control/iif.h"
#include "CompilerQOR/Preprocessor/preprocessor/facilities/identity.h"
#include "CompilerQOR/Preprocessor/preprocessor/punctuation/is_begin_parens.h"
#include "CompilerQOR/Preprocessor/preprocessor/tuple/enum.h"

#define QOR_PP_REMOVE_PARENS( param )                                                                                  \
	QOR_PP_IIF( QOR_PP_IS_BEGIN_PARENS( param ), QOR_PP_REMOVE_PARENS_DO, QOR_PP_IDENTITY )                            \
	( param )() \
/**/

#define QOR_PP_REMOVE_PARENS_DO( param )                                                                               \
	QOR_PP_IDENTITY( QOR_PP_TUPLE_ENUM( param ) ) \
/**/

#endif//QOR_PP_VARIADICS
#endif // QOR_PREPROCESSOR_REMOVE_PARENS_H
