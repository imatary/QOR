	double CBattery::GetTemperatureDegreesC()
	{
		_WINQ_FCONTEXT( "CBattery::GetTemperatureDegreesC" );
		InternalGetTemperature();

		double dResult = ((double)m_ulTemperature) - 273.15;
		dResult /= 10;
		return dResult;
	}

